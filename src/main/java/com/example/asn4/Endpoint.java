package com.example.asn4;

public class Endpoint {
    // to represent and return end point of a line like in lab
    private double x, y;
    public Endpoint(double x, double y){
        this.x = x;
        this.y = y;
    }

    public double getX() {
        return x;
    }

    public double getY() {
        return y;
    }
}
